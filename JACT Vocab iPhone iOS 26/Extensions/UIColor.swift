import UIKit

/// "Thematic" color constants.
extension UIColor {
    class var myGolden: UIColor {
        return self.init(red: 1.000, green: 0.894, blue: 0.541, alpha: 0.900)
    }
    class var myPaler: UIColor {
        return self.init(red: 1.000, green: 0.996, blue: 0.901, alpha: 1.000)
    }
}
